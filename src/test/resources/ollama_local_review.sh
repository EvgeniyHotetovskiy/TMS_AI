#!/bin/sh
set -euo pipefail

# Модель по умолчанию
MODEL="gemma:latest"

# Весь код приходит в $1 (а не через stdin!)
CODE="$1"

# Проверка, что код действительно передан
if [ -z "$CODE" ]; then
  echo "❌ Нет входного кода. Передай его как аргумент."
  exit 1
fi

# Формируем промпт
PROMPT="Проанализируй приведённый ниже код максимально глубоко и профессионально.
Дай развёрнутый, структурированный и практический код-ревью, ориентируясь на лучшие практики разработки, архитектуры и читаемости.

Обязательно включи следующие аспекты:

1. Потенциальные ошибки и скрытые баги:
   - логические ошибки
   - проблемы с обработкой исключений
   - ошибки в условиях, циклах, ветвлениях
   - некорректная работа с null, коллекциями, ресурсами

2. Архитектурные и структурные проблемы:
   - нарушение принципов SOLID
   - избыточная связанность
   - дублирование логики
   - неправильная ответственность классов и методов
   - проблемы расширяемости и тестируемости

3. Улучшение читаемости и поддержки:
   - нейминг переменных, методов, классов
   - длина методов
   - избыточные конструкции
   - упрощение логики
   - форматирование и стиль

4. Производительность и оптимизация:
   - лишние операции
   - неоптимальные структуры данных
   - потенциальные утечки ресурсов

5. Безопасность:
   - работа с пользовательскими данными
   - возможные уязвимости
   - небезопасные вызовы

6. Конкретные рекомендации:
   - что именно переписать и как
   - примеры улучшённого кода
   - альтернативные подходы

7. Итоговая оценка качества кода:
   - сильные стороны
   - слабые стороны
   - приоритетный список улучшений

8. Ревью должно быть на русском языке

КОД ДЛЯ АНАЛИЗА:
$CODE
"

# Передаём промпт в модель
printf "%s" "$PROMPT" | ollama run "$MODEL"
